package com.runwaysdk.geodashboard.oda.driver.ui.ssl;

public class SecureKeystoreException extends RuntimeException
{

  /**
   * 
   */
  private static final long serialVersionUID = -7391183495016705126L;

  public SecureKeystoreException(String message)
  {
    super(message);
  }

  public SecureKeystoreException(Throwable cause)
  {
    super(cause);
  }

  public SecureKeystoreException(String message, Throwable cause)
  {
    super(message, cause);
  }

}
